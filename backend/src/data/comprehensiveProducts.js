// Helper function to get products by category
export const getProductsByCategory = (category) => {
    return comprehensiveProducts.filter(product => product.category === category);
};

// Helper function to get featured products
export const getFeaturedProducts = () => {
    return comprehensiveProducts.filter(product => product.featured === true);
};

// Helper function to get products by scent family
export const getProductsByScentFamily = (scentFamily) => {
    return comprehensiveProducts.filter(product => product.scentFamily === scentFamily);
};

// Helper function to get products by price range
export const getProductsByPriceRange = (minPrice, maxPrice) => {
    return comprehensiveProducts.filter(product =>
        product.price >= minPrice && product.price <= maxPrice
    );
};
